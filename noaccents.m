function  s = noaccents(s)
%NOACCENTS - Convert LATIN-1 string to ASCII (remove accents)
%   [S] = noaccents(str)
%
%   Example
%       >> noaccents
%
%   See also:

% Author: K. N'Diaye (kndiaye01<at>yahoo.fr)
% Copyright (C) 2008
